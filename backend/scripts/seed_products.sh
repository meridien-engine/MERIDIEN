BASE_URL="http://localhost:8080/api/v1"
EMAIL="admin@meridien.com"
PASSWORD="Admin123"

echo "🔐 Logging in..."
GENERIC_TOKEN=$(curl -s -X POST "$BASE_URL/auth/login" \
  -H "Content-Type: application/json" \
  -d "{\"email\":\"$EMAIL\",\"password\":\"$PASSWORD\"}" \
  | python3 -c "import sys,json; d=json.load(sys.stdin); print(d['data']['token'])")

echo "🏢 Fetching businesses..."
BIZ_ID=$(curl -s -H "Authorization: Bearer $GENERIC_TOKEN" "$BASE_URL/auth/businesses" \
  | python3 -c "import sys,json; print(json.load(sys.stdin)['data'][0]['id'])")

echo "   Business ID: $BIZ_ID"

echo "🔑 Getting scoped token..."
SCOPED_TOKEN=$(curl -s -X POST "$BASE_URL/auth/use-business/$BIZ_ID" \
  -H "Authorization: Bearer $GENERIC_TOKEN" \
  | python3 -c "import sys,json; print(json.load(sys.stdin)['data']['token'])")

echo "📦 Seeding 100 hardware products..."

create_product() {
  local name="$1"
  local sku="$2"
  local price="$3"
  local cost="$4"
  local stock="$5"
  local desc="$6"

  RESULT=$(curl -s -X POST "$BASE_URL/products" \
    -H "Authorization: Bearer $SCOPED_TOKEN" \
    -H "Content-Type: application/json" \
    -d "{
      \"name\": \"$name\",
      \"sku\": \"$sku\",
      \"selling_price\": \"$price\",
      \"cost_price\": \"$cost\",
      \"stock_quantity\": $stock,
      \"track_inventory\": true,
      \"low_stock_threshold\": 5,
      \"description\": \"$desc\"
    }")

  STATUS=$(echo "$RESULT" | python3 -c "import sys,json; print(json.load(sys.stdin).get('success','false'))" 2>/dev/null)
  if [ "$STATUS" = "True" ] || [ "$STATUS" = "true" ]; then
    echo "  ✅ $name"
  else
    ERR=$(echo "$RESULT" | python3 -c "import sys,json; print(json.load(sys.stdin).get('error','unknown'))" 2>/dev/null)
    echo "  ⚠️  $name — $ERR"
  fi
}

